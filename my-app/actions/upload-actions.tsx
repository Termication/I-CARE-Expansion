'use server'

import { extractTextFromPdf } from "@/lib/langchain";
import type { ClientUploadedFileData } from "uploadthing/types";
import { generateFromOpenAI } from "@/lib/openai";
import { generateSummaryFromGemini } from "@/lib/geminiai";

export async function generatePdfSummary(uploadResponse: ClientUploadedFileData<{
  userId: string;
  fileUrl: string;
  fileName: string;
}>[]) {
  if (!uploadResponse || uploadResponse.length === 0) {
    return {
      success: false,
      message: "No upload response provided.",
      data: null,
    };
  }

  const { serverData: { fileUrl } } = uploadResponse[0];

  if (!fileUrl) {
    return {
      success: false,
      message: "No PDF URL provided.",
      data: null,
    };
  }

  try {
    const pdfText = await extractTextFromPdf(fileUrl);
    let summary;

    try {
      summary = await generateFromOpenAI(pdfText);
    } catch (error) {
      if (error instanceof Error && error.message === "Rate limit reached") {
        try {
          summary = await generateSummaryFromGemini(pdfText);
          return {
            success: true,
            message: "Summary generated by Gemini.",
            data: summary,
          };
        } catch (geminiError) {
          console.error("Gemini error:", geminiError);
          return {
            success: false,
            message: "Both OpenAI and Gemini failed to generate a summary.",
            data: null,
          };
        }
      }

      // Catch any other OpenAI error
      console.error("OpenAI summary error:", error);
      return {
        success: false,
        message: "Error generating summary from OpenAI.",
        data: null,
      };
    }

    return {
      success: true,
      message: "Summary generated by OpenAI.",
      data: summary,
    };
  } catch (err) {
    console.error("PDF extraction error:", err);
    return {
      success: false,
      message: "Error extracting text from PDF.",
      data: null,
    };
  }
}
